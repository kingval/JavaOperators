package C.Operators;

public class RelationalOperators {

    public static void main(String[] args) {

        int a=50;
        int b=40;

        System.out.println(a==b); //false
        System.out.println(a!=b); //true
        System.out.println(a>b); //false
        System.out.println(a<b); //true
        System.out.println(a>=b); //false
        System.out.println(a<=b); //true
    }
}
