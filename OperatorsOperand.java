package C.Operators;

public class OperatorsOperand {

    public static void main(String[] args) {

        int a=10;
        int b=20;

        System.out.println(a=b);  //assigning new value to the left operation (operand)  A = 40
        System.out.println(a+=b);
        System.out.println(a-=b);
        System.out.println(a*=b);
        System.out.println(a/=b);
        System.out.println(a%=b);
        System.out.println(a^=b);


    }
}
